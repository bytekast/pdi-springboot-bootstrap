package com.bytekast.pdi.bootstrap.trans.steps.web.model;

/**
 * @author Rowell Belen
 */
public class ThinCreditCardEntry {

  private String type;

  private int length;

  private int count;

  public String getType() {
    return type;
  }

  public void setType(String type) {
    this.type = type;
  }

  public int getLength() {
    return length;
  }

  public void setLength(int length) {
    this.length = length;
  }

  public int getCount() {
    return count;
  }

  public void setCount(int count) {
    this.count = count;
  }
}
